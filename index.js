document.addEventListener('DOMContentLoaded', showButton)

function showButton() {
  const headers = document.getElementById('headers')

  headers.innerHTML += '<button type="button" name="button">Show Artists</button>'

  const button = document.querySelector('button')

  button.addEventListener('click', bandInfo)
}


function bandInfo() {
  let bandList = document.getElementById('band-list')
  console.log("BANDLIST", bandList)
  nineties().forEach((band) => {
    const newDiv = document.createElement('div')
    newDiv.innerHTML = bandTemplate(band)
    bandList.appendChild(newDiv)
    // bandList.innerHTML += bandTemplate(band)
  })
}

function bandTemplate({name, bio, picture, video}) {
  return `<div class="band"><h3>${name}</h3><p>${bio}</p><img src='${picture}'/><iframe width="560" height="315" src="https://www.youtube.com/embed/${video.split('=')[1]}" frameborder="0" allow="autoplay; encrypted-media" allowfullscreen></iframe></div>`
}

